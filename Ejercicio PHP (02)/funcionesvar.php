<?php
function Suma($a,$b){
    $res = $a + $b;
    return $res;
}
function Resta($a,$b){
    $res = $a - $b;
    return $res;
}
function Division($a,$b){
    $res = $a / $b;
    return $res;
}
function Multiplicacion($a,$b){
    $res = $a * $b;
    return $res;
}
function Modulo($a,$b){
    $res = $a % $b;
    return $res;
}
function Potencia($a,$b){
    $res = $a ** $b;
    return $res;
}

?>